export function CreatorCard({
  id, displayName, slug, avatarUrl, location, totalFollowers,
  startingPrice, categories, verificationStatus, platforms,
}: CreatorCardProps) {
  return (
    <Link href={`/creators/${slug}`} className="block">
      <div className="card-elevated group cursor-pointer">
        {/* Image */}
        <div className="relative aspect-[3/4] bg-gray-100 overflow-hidden">
          {avatarUrl ? (
            <img
              src={avatarUrl}
              alt={displayName}
              className="w-full h-full object-cover group-hover:scale-105 transition-transform duration-500"
              loading="lazy"
            />
          ) : (
            <div className="w-full h-full bg-gradient-to-br from-purple-200 to-pink-200 flex items-center justify-center">
              <span className="text-4xl font-bold text-purple-400">
                {displayName.charAt(0)}
              </span>
            </div>
          )}
        </div>

        {/* Content */}
        <div className="p-4 space-y-3">
          {/* Name + Verified + Followers */}
          <div className="flex items-start justify-between">
            <div className="flex items-center gap-1.5">
              <h3 className="font-display font-semibold text-text-primary text-sm leading-tight">
                {displayName}
              </h3>
              {verificationStatus === 'VERIFIED' && (
                <BadgeCheck className="w-4 h-4 text-lime-500 flex-shrink-0" />
              )}
            </div>
            <div className="text-right flex-shrink-0 ml-2">
              <span className="text-sm font-bold text-text-primary">{formatFollowers(totalFollowers)}</span>
              <p className="text-[10px] text-text-secondary leading-tight">Followers</p>
            </div>
          </div>

          {/* Location */}
          {location && (
            <div className="flex items-center gap-1 text-text-secondary">
              <MapPin className="w-3 h-3" />
              <span className="text-xs">{location}</span>
            </div>
          )}

          {/* Platform icons */}
          <div className="flex items-center gap-2">
            {platforms.map((p) => (
              <span key={p} className="text-gray-400 hover:text-gray-600 transition-colors">
                {platformIcons[p] || null}
              </span>
            ))}
            {categories[0] && (
              <span className="ml-auto badge-lime text-[10px]">{categories[0]}</span>
            )}
          </div>

          {/* Price */}
          <div className="flex items-center justify-between pt-1 border-t border-gray-100">
            <div>
              <p className="text-[10px] text-text-secondary">Advertising Price</p>
              <p className="text-base font-bold font-display text-text-primary">
                {startingPrice ? formatCurrency(startingPrice) : 'Contact'}
              </p>
            </div>
          </div>

          {/* Actions */}
          <div className="flex items-center gap-2">
            <button className="p-2 rounded-full hover:bg-gray-100 transition-colors text-gray-400">
              <Heart className="w-4 h-4" />
            </button>
            <button className="p-2 rounded-full hover:bg-gray-100 transition-colors text-gray-400">
              <Bookmark className="w-4 h-4" />
            </button>
            <button className="ml-auto btn-primary text-xs py-2 px-4 flex items-center gap-1.5"
              onClick={(e) => { e.preventDefault(); }}>
              <MessageSquare className="w-3.5 h-3.5" />
              Send message
            </button>
          </div>
        </div>
      </div>
    </Link>
  );
}

